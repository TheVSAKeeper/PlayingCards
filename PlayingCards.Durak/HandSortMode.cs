namespace PlayingCards.Durak;

/// <summary>
/// Режим сортировки карт в руке игрока.
/// </summary>
/// <remarks>
/// Влияет только на порядок отображения карт (а значит и на их индексы в руке);
/// на правила игры не влияет — движок оперирует идентичностью карт, а не позицией.
/// </remarks>
public enum HandSortMode
{
    /// <summary>
    /// По старшинству; внутри одного ранга козырь — крайний правый; затем по масти.
    /// </summary>
    /// <remarks>Поведение по умолчанию, существовавшее до появления выбора режима.</remarks>
    ByRankTrumpInline = 0,

    /// <summary>
    /// Сначала весь некозырный веер по старшинству, затем все козыри отдельным блоком в конце.
    /// </summary>
    TrumpsSeparated = 1,

    /// <summary>
    /// Группировка по мастям; внутри масти по старшинству; козырная масть — последней.
    /// </summary>
    BySuit = 2,
}
